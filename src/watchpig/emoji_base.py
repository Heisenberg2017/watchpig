def init_emoji_map():
    emoji_map = {}
    all_unicode = [globals()[i] for i in globals().keys() if i.startswith("UNICODE")]
    for unicode in all_unicode:
        for i in unicode.split('\n'):
            if not i:
                continue
            emoji_map[i.split(' ', 1)[1]] = i.split(' ', 1)[0]
    return emoji_map


